    private void btnReserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReserveActionPerformed
        Date date = (Date) this.cbxDateReverse.getSelectedItem();
        Faculty faculty = (Faculty) this.cbxFacultyReverse.getSelectedItem();
        String seats = this.txtSeats.getText();
        if (date == null || faculty == null || !StringUtils.isInteger(seats)) {
            MessageUtil.showWarning(Constants.MESSAGE.FILL_ALL_FIELDS);
            return;
        }
        int existedId = ReservationDAO.checkReservationExists(date.getId(), faculty.getId());
        if (existedId > 0) {
            Object[] options = {"Yes", "No"};
            int btnClicked = JOptionPane.showOptionDialog(this, String.format(Constants.MESSAGE.CONFIRM_UPDATE_RESERVATION, faculty.getName(), CommonUtil.getDateFormat(date.getDate())), "Confirm update",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);
            if (btnClicked == JOptionPane.YES_OPTION) {
                ReservationService.updateReservation(date, Integer.valueOf(seats), existedId, faculty);
            }
        } else {
            ReservationService.addReservation(date, Integer.valueOf(seats), faculty);
        }
        showReservationList();
    }//GEN-LAST:event_btnReserveActionPerformed

