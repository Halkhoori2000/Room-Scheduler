    public void getReservations() {
        Faculty faculty = (Faculty) this.cbxReservationFilterFaculty.getSelectedItem();
        Date date = (Date) this.cbxReservationFilterDate.getSelectedItem();
        Room room = (Room) this.cbxReservationFilterRoom.getSelectedItem();
        ComboboxItem status = (ComboboxItem) this.cbxReservationFilterStatus.getSelectedItem();
        System.out.println("Searching reservations faculty:" + faculty + "/date:" + date + "/status:" + status + "/room:" + room);
        if (faculty != null && date != null && room != null && status != null) {
            List<ReservationDTO> reservations = ReservationDAO.search(faculty.getId(), date.getId(), status.getValue(), room.getId());
            ((CustomTable) this.tblWaiting).updateData(reservations);
        }
    }

    private void showReservationList() {
        this.cbxReservationFilterFaculty.removeAllItems();
        this.cbxReservationFilterFaculty.addItem(AllFaculty.getInstance());
        List<Faculty> lstFaculty = FacultyDAO.getAll();
        lstFaculty.stream().forEach(faculty -> this.cbxReservationFilterFaculty.addItem(faculty));

        this.cbxReservationFilterDate.removeAllItems();
        this.cbxReservationFilterDate.addItem(AllDate.getInstance());
        List<Date> lstDate = DateDAO.getAll();
        lstDate.stream().forEach(date -> this.cbxReservationFilterDate.addItem(date));

        this.cbxReservationFilterRoom.removeAllItems();
        this.cbxReservationFilterRoom.addItem(AllRoom.getInstance());
        List<Room> lstRoom = RoomDAO.getAll();
        lstRoom.stream().forEach(room -> this.cbxReservationFilterRoom.addItem(room));

        CommonUtil.showItem(this.pnlMain, this.pnlReservationList);
        getReservations();
    }

    private void showRoomList() {
        CommonUtil.showItem(this.pnlMain, this.pnlRoomList);
        List<Room> rooms = RoomDAO.getAll();
        ((CustomTable) this.tblRoom).updateData(rooms);
    }

